--v function()
local function initMCMrevive()
    local mcm = _G.mcm
    if not not mcm then
        local revive = mcm:register_mod("revive_region", "revive Region", "Adds the possibility to revive a settlement.")
        local restriction = revive:add_tweaker("penalty", "Public Order - Penalty", "Enable/Disable the public order penalty for reviveing one of your regions.")
        restriction:add_option("penalty", "Public Order - Penalty", "If you choose to enact a scorched earth policy you have to suffer the consequences.")
        restriction:add_option("nopenalty", "No Penalty", "reviveing Regions has no consequences!")
        local delay = revive:add_tweaker("delay", "Turns until Regions are reviveed", "Choose between instant and single turn delay until Regions are reviveed.")
        delay:add_option("instant", "Instant", "Regions can be reviveed instantly.")
        delay:add_option("oneTurn", "One Turn", "reviveing a region takes one turn.")
        mcm:add_post_process_callback(
            function()
                penalty = cm:get_saved_value("mcm_tweaker_revive_region_penalty_value")
                if penalty == "penalty" then
                    penaltyEnable = true
                elseif penalty == "nopenalty" then
                    penaltyEnable = false
                end
            end
        )
    else
        local penalty_value = cm:get_saved_value("mcm_tweaker_revive_region_penalty_value")
        if penalty_value == "nopenalty" then
            penaltyEnable = false
        else
            penaltyEnable = true
        end
    end
end
