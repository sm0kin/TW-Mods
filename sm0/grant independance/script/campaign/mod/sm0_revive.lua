local function createreviveButton()
    if not reviveButton then
        reviveButton = Button.new("reviveButton", find_uicomponent(core:get_ui_root(), "settlement_panel"), "SQUARE", iconPath)
        local renameButton = find_uicomponent(core:get_ui_root(), "settlement_panel", "button_rename")
        reviveButton:Resize(renameButton:Width(), renameButton:Height())
        local abandonButton = find_uicomponent(core:get_ui_root(), "settlement_panel", "abandonButton")
        if abandonButton then
            reviveButton:PositionRelativeTo(abandonButton, abandonButton:Width() + 1, 0)
        else
        reviveButton:PositionRelativeTo(renameButton, renameButton:Width() + 1, 0)
        end
        reviveButton:SetState("hover")
        reviveButton.uic:SetTooltipText(reviveButtonTooltip)
        reviveButton:SetState("active")
        reviveButton:RegisterForClick(
            function(context)
                reviveRegionStr = regionStr
                createreviveFrame(reviveRegionStr)
            end 
        )		
    end
end

