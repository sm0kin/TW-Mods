            local faction = cm:model():faction_for_command_queue_index(context:faction_cqi()):name()
            local regionNameEnd = string.find(info, "<")
            local regionName = string.sub(info, 1, regionNameEnd - 1)
            local regionTorevive = cm:get_region(regionName)
            local cashEnd = string.find(info, ">")
            local cash = tonumber(string.sub(info, regionNameEnd + 1, cashEnd - 1))
            local penaltyEnd = string.find(info, "~")
            local penalty = string.sub(info, cashEnd + 1, penaltyEnd - 1)
            local remove = string.sub(info, penaltyEnd + 1)
            
            if cm:get_saved_value("mcm_tweaker_revive_region_delay_value") ~= "oneTurn" then
                cm:show_message_event(
                    faction,
                    "event_feed_targeted_events_title_provinces_settlement_reviveedevent_feed_target_settlement_faction",
                    "regions_onscreen_"..regionName,
                    "event_feed_strings_text_wh_event_feed_string_provinces_settlement_reviveed_description",
                    true,
                    id_from_subculture[cm:get_faction(faction):subculture()]
                )
                reviveFaction()
                cm:treasury_mod(faction, cash)
                if penalty == "true" then cm:apply_effect_bundle("wh2_sm0_revive_public_order_down", faction, 5) end
            else
                if remove == "remove" then
                    core:remove_listener("revive_"..regionName.."_"..faction)
                    cm:set_saved_value("revive_"..regionName.."_"..faction, false)
                else
                    cm:set_saved_value("revive_"..regionName.."_"..faction, context:trigger())
                    core:add_listener(
                        "revive_"..regionName.."_"..faction,
                        "FactionTurnStart",
                        function(context)
                            return context:faction():name() == faction
                        end,
                        function(context)
                            local region = cm:get_region(regionName)
                            local regionOwner = region:owning_faction()
                            if regionOwner:name() == context:faction():name() then
                                cm:show_message_event(
                                    faction,
                                    "event_feed_targeted_events_title_provinces_settlement_reviveedevent_feed_target_settlement_faction",
                                    "regions_onscreen_"..regionName,
                                    "event_feed_strings_text_wh_event_feed_string_provinces_settlement_reviveed_description",
                                    true,
                                    id_from_subculture[cm:get_faction(faction):subculture()]
                                )
                                reviveFaction()
                                cm:treasury_mod(faction, cash)
                                if penalty == "true" then cm:apply_effect_bundle("wh2_sm0_revive_public_order_down", faction, 5) end
                                cm:set_saved_value("revive_"..regionName.."_"..regionOwner:name(), false)
                            end
                        end,
                        false
                    )
                end
            end
        end,
        true
    )

    if cm:get_saved_value("mcm_tweaker_revive_region_delay_value") == "oneTurn" then
        local regionList = playerFaction:region_list()
        for i = 0, regionList:num_items() - 1 do
            local currentRegion = regionList:item_at(i)
            if cm:get_saved_value("revive_"..currentRegion:name().."_"..playerFaction:name()) then
                CampaignUI.TriggerCampaignScriptEvent(cm:get_faction(cm:get_local_faction(true)):command_queue_index(), cm:get_saved_value("revive_"..currentRegion:name().."_"..playerFaction:name()))
            end
        end
    end
